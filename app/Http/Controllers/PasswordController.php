                'password.min' => 'Le mot de passe doit faire au moins 6 caractÃ¨res!',
                'password.required' => 'Mot de passe obligatoire'
            ]);

        $user = User::find($id);
        $user->password   = bcrypt($request->password);
        $user->update();

        return redirect(url('/profile'));


